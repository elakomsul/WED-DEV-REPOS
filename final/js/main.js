$(".fadein").hover( function() {
		
	// this gets called when you hover over
	var $slideIn = $(this).find('.slide-in');
	var $slideOut = $(this).find('.slide-out');

	$slideIn
		.animate({
			left: '0',
			opacity: 1
		}, 350);

	$slideOut
		.animate({
			left: '-227px',
			opacity: 0
		}, 350);

}, function() {
		
	// this gets called when you hover off
	var $slideIn = $(this).find('.slide-in');
	var $slideOut = $(this).find('.slide-out');
	
	$slideIn
		.animate({
			left: '227px',
			opacity: 0
		}, 350);

	$slideOut
		.animate({
			left: '0',
			opacity: 1
		}, 350);
	});

